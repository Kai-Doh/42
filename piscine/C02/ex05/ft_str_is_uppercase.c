int	ft_str_is_uppercase(char *str)
{
	int	x;

	x = 0;
	while (str[x] >= 65 && str[x] <= 90)
	{
		x++;
	}
	if (str[x] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
