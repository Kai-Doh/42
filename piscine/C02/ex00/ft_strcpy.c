char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	if (dest[i] == '\0' || src[i] == '\0')
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
